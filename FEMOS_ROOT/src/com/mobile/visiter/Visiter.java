package com.mobile.visiter;

public class Visiter {
	private String visiterId = "";
	private String visiterNm = "";
	private String sex = "";
	private String hp = "";
	private String email = "";
	private String qrcode = "";
	
	public String getVisiterId() {
		return visiterId;
	}
	public void setVisiterId(String visiterId) {
		this.visiterId = visiterId;
	}
	public String getVisiterNm() {
		return visiterNm;
	}
	public void setVisiterNm(String visiterNm) {
		this.visiterNm = visiterNm;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String hp) {
		this.hp = hp;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getQrcode() {
		return qrcode;
	}
	public void setQrcode(String qrcode) {
		this.qrcode = qrcode;
	}
}
