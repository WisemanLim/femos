package com.mobile.missing;

/**
 * @author wisemanlim
 *
 */
public class MissingInfo {

	private int serial;
	private String name;
	private String protector;
	private String protectorTel;
	private int rSerial;
	private int missingKey;
	private String result;
	
	/**
	 * @return the serial
	 */
	public int getSerial() {
		return serial;
	}

	/**
	 * @param serial the serial to set
	 */
	public void setSerial(int serial) {
		this.serial = serial;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the protector
	 */
	public String getProtector() {
		return protector;
	}

	/**
	 * @param protector the protector to set
	 */
	public void setProtector(String protector) {
		this.protector = protector;
	}

	/**
	 * @return the protectorTel
	 */
	public String getProtectorTel() {
		return protectorTel;
	}

	/**
	 * @param protectorTel the protectorTel to set
	 */
	public void setProtectorTel(String protectorTel) {
		this.protectorTel = protectorTel;
	}
	
	/**
	 * @return the rSerial
	 */
	public int getRSerial() {
		return rSerial;
	}

	/**
	 * @param rSerial the rSerial to set
	 */
	public void setRSerial(int rSerial) {
		this.rSerial = rSerial;
	}

	/**
	 * @return the missingKey
	 */
	public int getMissingKey() {
		return missingKey;
	}

	/**
	 * @param missingKey the missingKey to set
	 */
	public void setMissingKey(int missingKey) {
		this.missingKey = missingKey;
	}

	/**
	 * @return the result
	 */
	public String getResult() {
		return result;
	}

	/**
	 * @param result the result to set
	 */
	public void setResult(String result) {
		this.result = result;
	}
}