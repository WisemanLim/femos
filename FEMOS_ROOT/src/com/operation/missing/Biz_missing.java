package com.operation.missing;

public class Biz_missing {
	
	private int rn = 0;
	private int serial = 0; 
	private String name = "";
	private String protector = "";
	private String protector_tel = "";
	private int result_cnt = 0;
	private int missing_key = 0;
	private String result = "";
	
	public Biz_missing(){}

	public int getRn() {
		return rn;
	}

	public void setRn(int rn) {
		this.rn = rn;
	}

	public int getSerial() {
		return serial;
	}

	public void setSerial(int serial) {
		this.serial = serial;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getProtector() {
		return protector;
	}

	public void setProtector(String protector) {
		this.protector = protector;
	}

	public String getProtector_tel() {
		return protector_tel;
	}

	public void setProtector_tel(String protector_tel) {
		this.protector_tel = protector_tel;
	}

	public int getResult_cnt() {
		return result_cnt;
	}

	public void setResult_cnt(int result_cnt) {
		this.result_cnt = result_cnt;
	}

	public int getMissing_key() {
		return missing_key;
	}

	public void setMissing_key(int missing_key) {
		this.missing_key = missing_key;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}
	
}
