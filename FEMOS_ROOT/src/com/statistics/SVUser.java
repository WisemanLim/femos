package com.statistics;

public class SVUser {
	private Integer seq = 0;
	private String userMailAddr = "";
	private String regYmd = "";
	private Integer age = 0;
	private String area = "";
	private String sex = "";
	private Integer recNum = 0;
	private Integer listNum = 0;
	
	/**
	 * @return the seq
	 */
	public Integer getSeq() {
		return seq;
	}
	/**
	 * @param seq the seq to set
	 */
	public void setSeq(Integer seq) {
		this.seq = seq;
	}
	/**
	 * @return the userMailAddr
	 */
	public String getUserMailAddr() {
		return userMailAddr;
	}
	/**
	 * @param userMailAddr the userMailAddr to set
	 */
	public void setUserMailAddr(String userMailAddr) {
		this.userMailAddr = userMailAddr;
	}
	/**
	 * @return the regYmd
	 */
	public String getRegYmd() {
		return regYmd;
	}
	/**
	 * @param regYmd the regYmd to set
	 */
	public void setRegYmd(String regYmd) {
		this.regYmd = regYmd;
	}
	/**
	 * @return the age
	 */
	public Integer getAge() {
		return age;
	}
	/**
	 * @param age the age to set
	 */
	public void setAge(Integer age) {
		this.age = age;
	}
	/**
	 * @return the area
	 */
	public String getArea() {
		return area;
	}
	/**
	 * @param area the area to set
	 */
	public void setArea(String area) {
		this.area = area;
	}
	/**
	 * @return the sex
	 */
	public String getSex() {
		return sex;
	}
	/**
	 * @param sex the sex to set
	 */
	public void setSex(String sex) {
		this.sex = sex;
	}
	/**
	 * @return the recNum
	 */
	public Integer getRecNum() {
		return recNum;
	}
	/**
	 * @param recNum the recNum to set
	 */
	public void setRecNum(Integer recNum) {
		this.recNum = recNum;
	}
	/**
	 * @return the listNum
	 */
	public Integer getListNum() {
		return listNum;
	}
	/**
	 * @param listNum the listNum to set
	 */
	public void setListNum(Integer listNum) {
		this.listNum = listNum;
	}
}
