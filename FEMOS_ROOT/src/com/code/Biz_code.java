package com.code;

public class Biz_code 
{
	private String code = "";
	private String code_nm = "";
	private String code_group = "";
	private String mean_code = "";
	private int sort_seq = 0;
	private String summary = "";
	private String text_color = "";
	
	public Biz_code(){}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getCode_nm() {
		return code_nm;
	}

	public void setCode_nm(String code_nm) {
		this.code_nm = code_nm;
	}

	public String getCode_group() {
		return code_group;
	}

	public void setCode_group(String code_group) {
		this.code_group = code_group;
	}

	public String getMean_code() {
		return mean_code;
	}

	public void setMean_code(String mean_code) {
		this.mean_code = mean_code;
	}

	public int getSort_seq() {
		return sort_seq;
	}

	public void setSort_seq(int sort_seq) {
		this.sort_seq = sort_seq;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public String getText_color() {
		return text_color;
	}

	public void setText_color(String text_color) {
		this.text_color = text_color;
	}
	
	
}
