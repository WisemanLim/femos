package com.plan.lodge;

public class Biz_lodge_main {
	
	private String lodge_no = "";
	private String festival_cd = "";
	private String lodge_nm = "";
	private String phone = "";
	private String addr = "";
	
	public String getLodge_no() {
		return lodge_no;
	}
	public void setLodge_no(String lodge_no) {
		this.lodge_no = lodge_no;
	}
	public String getFestival_cd() {
		return festival_cd;
	}
	public void setFestival_cd(String festival_cd) {
		this.festival_cd = festival_cd;
	}
	public String getLodge_nm() {
		return lodge_nm;
	}
	public void setLodge_nm(String lodge_nm) {
		this.lodge_nm = lodge_nm;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	
}
