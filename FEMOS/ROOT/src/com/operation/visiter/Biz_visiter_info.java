package com.operation.visiter;

public class Biz_visiter_info 
{
	private String visiter_id = "";
	private String visiter_nm = "";
	private String sex = "";
	private String hp = "";
	private String email = "";
	
	public String getVisiter_id() {
		return visiter_id;
	}
	public void setVisiter_id(String visiter_id) {
		this.visiter_id = visiter_id;
	}
	public String getVisiter_nm() {
		return visiter_nm;
	}
	public void setVisiter_nm(String visiter_nm) {
		this.visiter_nm = visiter_nm;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String hp) {
		this.hp = hp;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
}
